public class ConcatenateString {
    public static void main(String[] args) {
        String nombre = "Victor";
        String apellidos = "Jamet";
        String codigoPostal = "123232";
        String calle =  "miCalle";
        char Sexo = 'M';
        String correo =  "victor@gmail.com";
        System.out.println(nombre + "\t" + apellidos + "\t" + codigoPostal + "\t" + calle + "\t" + Sexo + "\t" + correo);
    }
}
